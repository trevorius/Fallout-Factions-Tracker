export const words = [...wordList];

export function generatePassword(): string {
  const getRandomWord = (words: string[]) => {
    const randomIndex = Math.floor(Math.random() * words.length);
    const randomWord = words[randomIndex];
    const newWords = [...words];
    newWords.splice(randomIndex, 1);
    return { word: randomWord, newWords };
  };

  const { word: word1, newWords: newWords1 } = getRandomWord(words);
  const { word: word2, newWords: newWords2 } = getRandomWord(newWords1);
  const { word: word3 } = getRandomWord(newWords2);

  return `${word1}-${word2}-${word3}`;
}
